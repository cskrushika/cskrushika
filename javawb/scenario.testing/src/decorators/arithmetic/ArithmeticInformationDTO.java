package decorators.arithmetic;

/**
 * Holds the scenario information for Arithmetic actions
 *
 */
public class ArithmeticInformationDTO {
	private float inputNumber;
	private float result;

	public float getResult() {
		return result;
	}

	public void setResult(float result) {
		this.result = result;
	}

	public float getInputNumber() {
		return inputNumber;
	}

	public void setInputNumber(float inputNumber) {
		this.inputNumber = inputNumber;
	}

}
